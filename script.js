function addLogWithEffect(text) {
  const li = document.createElement("li");
  logList.appendChild(li);

  let i = 0;
  const interval = setInterval(() => {
    li.textContent += text[i];
    i++;
    if (i >= text.length) {
      clearInterval(interval);
    }
    logList.scrollTop = logList.scrollHeight;
  }, 40);
}


